/*
   流程控制语句之for循环
   语法：
       for(表达式1;表达式2;表达式3){
	循环体代码块
      }
   说明：
       a)表达式2的结果必须是boolean类型的值(true/false)
*/
public class ForStruct {
   public static void main(String[] args) {
       for(int i=1;i<=10;i++){
             System.out.println("i="+i);
       }
   }
}




